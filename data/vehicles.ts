import { ImageSourcePropType } from 'react-native';

export type FuelType = 'Petrol' | 'Diesel' | 'Hybrid' | 'Electric';
export type Transmission = 'Automatic' | 'Manual';
export type VehicleType =
  | 'Sedan'
  | 'SUV'
  | 'Hatchback'
  | 'Coupe'
  | 'Convertible'
  | 'Pickup'
  | 'Van'
  | 'EV Sedan';

export interface Vehicle {
  id: string;
  /** Display title for cards (kept to align with current UI use) */
  title: string;

  // Vehicle details
  make: string;
  model: string;
  year: number;
  color: string;
  type: VehicleType;
  transmission: Transmission;
  fuelType: FuelType;
  /** EV only */
  evRangeKm?: number;

  seats: number;
  doors: number;

  /** Area/neighborhood in Cape Town */
  locationArea: string;
  /** Coordinates for maps */
  lat: number;
  lng: number;

  /** Pricing: keep a display string to avoid touching existing UI */
  pricePerDay: number; // numeric for logic/calcs
  priceDisplay: string; // e.g., "R 200/day"

  image: ImageSourcePropType;

  /** Optional UI niceties */
  rating?: number; // 0..5
  features?: string[];
}

export interface VehicleSection {
  title: string;
  vehicles: Vehicle[];
}

